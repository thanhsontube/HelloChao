public class IdiomEntity {
    private String idiomKey;
    private String idiomValue;
    private String idiomSample;

    public IdiomEntity() {
    }

    public String getIdiomKey() {
        return idiomKey;
    }

    public void setIdiomKey(String idiomKey) {
        this.idiomKey = idiomKey;
    }

    public String getIdiomValue() {
        return idiomValue;
    }

    public void setIdiomValue(String idiomValue) {
        this.idiomValue = idiomValue;
    }

    public String getIdiomSample() {
        return idiomSample;
    }

    public void setIdiomSample(String idiomSample) {
        this.idiomSample = idiomSample;
    }
}
